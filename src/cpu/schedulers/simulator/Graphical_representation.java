    private void runActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_runActionPerformed
        // TODO add your handling code here:
        model.setRowCount(0);
        /*Technique 1*/
        JLabel labName;
        int pos = 50;
        int shift = 0;
        JLabel labIcon;
        for(int i=0 ; i<tech1.processes.size() ; ++i){
            labName = new JLabel();
            labIcon = new JLabel();
            labName.setText(tech1.processes.get(i).name);
            NP_SJF.add(labName);
            labName.setBounds(new Rectangle(new Point(10,i*pos+10) , labName.getPreferredSize()));
            
            labIcon.setIcon(new ImageIcon("D:\\Education\\Java\\CPU-Schedulers-Simulator\\colors" + "\\"+ tech1.processes.get(i).color + ".jpg"));
            NP_SJF.add(labIcon);
            labIcon.setBounds(new Rectangle(new Point(60 + shift ,i*pos+10) , labIcon.getPreferredSize()));
            labIcon.setSize( tech1.processes.get(i).burstTime*20 ,15);
            shift += tech1.processes.get(i).burstTime*20;
        }
        
        /*Table*/
        int PID = 3280;
        for (int i=0 ; i < obj.in.numberOfProcesses ; i++){
            model.insertRow(tblData.getRowCount(), new Object[]{i+1,obj.in.colors.get(i),obj.in.names.get(i)
                            ,PID+i,obj.in.priorityNumbers.get(i)});
        }
        /*AWT*/
        AWT.setText(String.format( "%.3f", tech1.averageWaitingTime));
        /*ATAT*/
        ATAT.setText(String.format( "%.3f",  tech1.averageTurnaroundTime));
    }//GEN-LAST:event_runActionPerformed

